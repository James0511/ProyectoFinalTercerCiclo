    private void GuardarAdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarAdminActionPerformed
        cedula = CedulaAdmin.getText();
        int bandera = 0;
        if (!CedulaAdmin.getText().matches("\\d{10}")) {
            JOptionPane.showMessageDialog(null, "Cedula Incorrecta");
            bandera = 1;
        }

        if (!Nombre.getText().matches("[a-zA-Z ]*\\S$")) {
            JOptionPane.showMessageDialog(null, "Nombre Invalido");
            bandera = 1;
        }

        if (!Apellido.getText().matches("[a-zA-Z ]*\\S$")) {
            JOptionPane.showMessageDialog(null, "Apellido Invalido");
            bandera = 1;
        }

        if (!Telefono.getText().matches("\\d{9}")) {
            JOptionPane.showMessageDialog(null, "Telefono Invalido");
            bandera = 1;
        }

        if (!Celular.getText().matches("\\d{10}")) {
            JOptionPane.showMessageDialog(null, "Celular Invalido");
            bandera = 1;
        }

        if (!Correo.getText().matches("[-\\w\\.]+@\\w+\\.\\w+")) {
            JOptionPane.showMessageDialog(null, "Correo Invalido");
            bandera = 1;
        }
        
        if (bandera == 0) {
            CrearPersonaSQL();
            CrearRegistro();
            BuscarCodRSQL();
            CrearAdminSQL();
        }
        
    }//GEN-LAST:event_GuardarAdminActionPerformed

    public void CrearPersonaSQL(){
        String url = "jdbc:postgresql://localhost:5432/postgres";
        String usuario = "postgres";
        String contrasenia = "1234";
        try{
            Class.forName("org.postgresql.Driver");
            Connection conexion = DriverManager.getConnection(url, usuario, contrasenia);
            java.sql.Statement st = conexion.createStatement();
            String sql
                    = "INSERT INTO persona(cedula, nombre, apellido, telefono, celular, correo) VALUES ('"+CedulaAdmin.getText()+"','"+Nombre.getText().toString()+"','"+Apellido.getText().toString()+"','"+Telefono.getText().toString()+"','"+Celular.getText().toString()+"','"+Correo.getText().toString()+"');";
            result = st.executeQuery(sql);
            result.close();
            st.close();
            conexion.close();
        }catch(Exception e){} 
    }
    
